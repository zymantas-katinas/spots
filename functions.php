function theme_scripts(){

    if ( !is_admin() ) {
		$key = get_field('google_maps_api', 'option');

    	//wp_register_script(handle, path, dependency, version, load_in_footer);
        wp_deregister_script('jquery');
	
		//Registration
		wp_register_script('fontawesome', "https://kit.fontawesome.com/1504a34cb6.js", false, false, false);
		wp_register_script('jquery', THEME_FOLDER . '/assets/scripts/jquery-3.4.1.min.js', array('fontawesome'), false, true);
		wp_register_script('mapjs', THEME_FOLDER . '/assets/scripts/map.js', array('jquery'), false, true);
		wp_register_script('mapapi', "https://maps.googleapis.com/maps/api/js?key=$key&language=EN&callback=initMap", array('mapjs'), false, true);
		wp_register_script('custom', THEME_FOLDER . '/assets/scripts/custom.js', array('mapapi'), false, true);
		
		//Loading
		wp_enqueue_script('fontawesome');
		wp_enqueue_script('jquery');
		wp_enqueue_script('mapjs');
		wp_enqueue_script('mapapi');		
		wp_enqueue_script('custom');
			
    }
}
add_action('wp_enqueue_scripts', 'theme_scripts');


